public class Oper_Logicos {

    public static void main(String[] args){

        boolean a = true, b = false;

        //Operador AND(Y)
        System.out.println(a && b);

        //Operador OR(O)
        System.out.println(a || b);

        //operador NOT (NO)
        System.out.println(!a);

        //XOR LOGICO
        System.out.println(a ^ b);
    }
}
