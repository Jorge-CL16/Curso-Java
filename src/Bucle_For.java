public class Bucle_For {

    public static void main(String[] args){


        //bucle ascendente
        for (int i = 1; i <= 10; i++){
            System.out.println("vuelta numero: " + i);
        }

        //bucle descendente
        for (int j = 10; j >= 0; j--){
            System.out.println("vuelta numero: " + j);
        }
        
    }
}
